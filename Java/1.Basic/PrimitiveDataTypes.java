public class PrimitiveDataTypes {
    public static void main(String args[]) {
        // Java DataType Example - Primitive

        // 1. Boolean
        boolean b = true;
        boolean b1 = false;
        System.out.println("Boolean DataTypes: ");
        System.out.println(b);
        System.out.println(b1);

        // 2. Byte
        byte by = 10;
        System.out.println("Byte DataTypes: ");
        System.out.println(by);

        // 3. Short
        short s = 100;
        System.out.println("Short DataTypes: ");
        System.out.println(s);

        // 4. Int
        int i = 1000;
        System.out.println("Int DataTypes: ");
        System.out.println(i);

        // 5. Long
        long l = 10000;
        System.out.println("Long DataTypes: ");
        System.out.println(l);

        // 6. Float
        float f = 10.5f;
        System.out.println("Float DataTypes: ");
        System.out.println(f);

        // 7. Double
        double d = 10.5;
        System.out.println("Double DataTypes: ");
        System.out.println(d);

        // 8. Char
        char c = 'A';
        System.out.println("Char DataTypes: ");
        System.out.println(c);

    }
}
