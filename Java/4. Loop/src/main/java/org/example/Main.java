package org.example;

public class Main {
    public static void main(String[] args) {
        int n = 20;

        for (int i = 0; i <= n; i++) {
            System.out.println(i);
        }

        int m = 10;
        int i = 0 ;
        while (i <= m){
            System.out.println(i);
            i++;
        }

    }
}