package org.example;

import java.util.*;

/*
* Local Variable :- A variable defined within the block of method pr constructor
* is called local variable.
* The scope of local variable only exist in that block.
* */

public class LocalVariable {
    public static void main(String[] args){
        int age = 24 ;
        String Name = "Sam";

        System.out.println(age);
        System.out.println(Name);
    }
}
