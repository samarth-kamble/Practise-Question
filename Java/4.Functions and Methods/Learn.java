class Learn {

    /*
     * Methods in Java
     * 
     * In Java, methods are blocks of code that perform a specific task.
     * They are used to execute code when called and can take parameters
     * and return values.
     * 
     * Syntax of a method:
     * 
     * returnType methodName(parameters) {
     * // method body
     * }
     * 
     * Return Type
     * 
     * The return type of a method specifies the type of value that the method
     * will return after execution. If a method does not return a value, its
     * return type is specified as "void".
     * 
     */

    public static void printMessage() {
        System.out.println("Hello from a method!");
    }

    /*
     * This is a simple method that prints a message to the console.
     * It demonstrates the use of methods in Java.
     */
    public static void main(String[] args) {
        printMessage();
    }
}